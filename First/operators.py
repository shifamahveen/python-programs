# Arithmetic operators -> +, -, *, /, //, **, %
print(2 + 3)    # Addition +
print(10 - 5)   # Subtraction -
print(5 * 10)   # Multiplication *
print(25 / 4)   # Division /
print(25 // 4)  # Integer division //
print(2 ** 3)   # Exponent operator
print(11 % 2)   # modulus operator - returns remainder after division