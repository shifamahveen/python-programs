# Arithmetic -> +, -, *, /, //, **, %

a = 10
b = 25
print(a+b)
print(a-b)
print(a*b)
print(b/a)
print(b//a)     # integer division
print(10 ** 2)   # exponent
print(9 % 5)       # modulus operator - returns remainder after division
                # if numerator is smaller, returns the numerator value